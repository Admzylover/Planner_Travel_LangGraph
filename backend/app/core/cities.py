def get_all_city_names() -> List[str]:
    """获取所有城市名称列表"""
    cities = []
    for province in CITIES_DATA:
        for city in province.cities:
            cities.append(city.name)
    return cities


def get_province_by_city(city_name: str) -> str:
    """根据城市名获取省份"""
    for province in CITIES_DATA:
        for city in province.cities:
            if city.name == city_name:
                return province.province
    return None


def get_adcode_by_city(city_name: str) -> str:
    """根据城市名获取adcode"""
    for province in CITIES_DATA:
        for city in province.cities:
            if city.name == city_name:
                return city.adcode
    return None


def is_valid_city(city_name: str) -> bool:
    """验证城市名是否有效"""
    return get_province_by_city(city_name) is not None